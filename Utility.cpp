
int Add(int First, int Second)
{
	return First + Second;
}

int Minus(int First, int Second)
{
	return First - Second;
}

int Mutifly(int First, int Second)
{
	return First * Second;
}

int Division(int First, int Second)
{
	return First / Second;
}

int Mod(int First, int Second)
{
	return First % Second;
}